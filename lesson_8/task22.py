# Решение:

f = open('dump.txt', 'r+', encoding='utf-8')
my_file = f.read()
f.close()

bukovki = ['а', 'о', 'у', 'ю', 'я', 'э', 'и', 'ы', 'ё','е']

for bukovka in bukovki:
    print(f'{bukovka} встретилось {my_file.count(bukovka)} раз')